# ── Pydantic request models ───────────────────────────────────────────────────

class DemoProvenance(BaseModel):
    model_config = ConfigDict(frozen=True, extra="ignore")
    source_id: Annotated[str, Field(max_length=100)]
    chain_of_custody: Annotated[list[str], Field(min_length=1, max_length=5)]
    classification: Literal[0, 1]


class DemoIngestRequest(BaseModel):
    model_config = ConfigDict(frozen=True, extra="forbid")
    data: Annotated[
        dict[str, str | int | float | bool | None],
        Field(max_length=20, description="Key-value payload; max 20 keys"),
    ]
    provenance: DemoProvenance
    purpose: Literal["billing-inquiry", "compliance-audit"]
    subject_id: Literal["user-demo"]


class DemoQueryRequest(BaseModel):
    model_config = ConfigDict(frozen=True, extra="forbid")
    purpose: Literal["billing-inquiry", "compliance-audit"]
    subject_ids: Annotated[
        list[Literal["user-demo"]],
        Field(min_length=1, max_length=1),
    ]
    classification_max: Literal[0, 1] = 1
    constraints: dict[str, str] | None = None


class DemoCommitRequest(BaseModel):
    model_config = ConfigDict(frozen=True, extra="forbid")
    event_type: Annotated[
        str,
        Field(
            pattern=r"^demo\.[a-z][a-z0-9._-]{0,98}$",
            description="Must start with 'demo.' e.g. demo.payment.approved",
        ),
    ]
    payload: Annotated[
        dict[str, str | int | float | bool | None],
        Field(max_length=20),
    ]


class DemoReviewCreateRequest(BaseModel):
    model_config = ConfigDict(frozen=True, extra="forbid")
    proposed_action: Annotated[str, Field(min_length=1, max_length=200)]
    reason: Annotated[str, Field(min_length=1, max_length=200)]
    autonomy_level: Annotated[int, Field(ge=1, le=5)] = 3
    risk_assessment: Annotated[str, Field(max_length=200)] = ""


class DemoGrantRequest(BaseModel):
    model_config = ConfigDict(frozen=True, extra="forbid")
    subject_id: Literal["user-demo"]
    grantee_id: Literal["demo-agent", "intruder-agent", "demo-human"]
    operations: Annotated[
        list[Literal["ingest", "query", "replay", "export"]],
        Field(min_length=1, max_length=4),
    ]
    purpose: Literal["billing-inquiry", "compliance-audit"]
    classification_max: Literal[0, 1] = 1


