# ── Custom OpenAPI — adds X-Demo-Actor security scheme ────────────────────────

def _build_openapi() -> dict:
    if app.openapi_schema:
        return app.openapi_schema
    schema = get_openapi(
        title=app.title,
        version=app.version,
        description=app.description,
        routes=app.routes,
    )
    schema.setdefault("components", {})
    schema["components"]["securitySchemes"] = {
        "DemoActor": {
            "type": "apiKey",
            "in": "header",
            "name": "X-Demo-Actor",
            "description": (
                "Identifies the acting entity. "
                "Values: `demo-agent`, `intruder-agent`, `demo-human`. "
                "Unknown values silently fall back to `demo-agent`."
            ),
        }
    }
    for path_data in schema.get("paths", {}).values():
        for operation in path_data.values():
            if isinstance(operation, dict):
                operation.setdefault("security", [{"DemoActor": []}])
    app.openapi_schema = schema
    return schema


app.openapi = _build_openapi  # type: ignore[method-assign]


